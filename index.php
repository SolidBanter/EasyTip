      <p class="body-para"> "EasyTip" is an app that makes the process of <b>giving and receiving tips</b> as simple as possible. During economic hardships, it is especially important to remember 
        those who work minimum wage jobs, and work hard to maintain their lifestyle. Even though this web app is designed for <i>restaurants</i> initially, it can be easily modified to be accessible by even more
        businesses such as <i>delivery systems, barbershops</i> and etc. It is also common sense that people are more likely to do something if it is an easy task to do. By making this process easier, 
        our goal is to encourage the citizens to tip more. And this is not a one-way app either: restaurants and business owners benefit as well, since they have access to the feedback that the customers give to each worker.  Here is how the app works: Users can search for restaurants in the "Search" page. 
        Once they found the restaurant, they can click on the waiter/waitress they had, and choose to tip them a specific amount.
        Waiters, when they register, should request an account linkage from the restaurant, so that users can find their name, and managers can see customer feedback. </p>

